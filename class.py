class add_sub:
    def __init__(self,x,y):
        self.x=x
        self.y=y
        def add(self):
            return self.x+self.y
            def subtract(self):
                return self.x-self.y

if __name__ ==' __main__':
    x=20
    y=50
    opp = add_sub(x,y)
    print(f'{x}+{y}={opp.add()}')
    print(f'{x}-{y}={opp.subtract()}')
    




